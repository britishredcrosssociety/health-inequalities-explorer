library(tidyverse)
library(sf)
library(geographr)
library(rmapshaper)

england_ltla_shp <-
  boundaries_ltla24 |>
  filter(str_detect(ltla24_code, "^E")) |>
  ms_simplify(keep = 0.5) |> # Any less and areas get dropped
  select(area_name = ltla24_name, area_code = ltla24_code)

usethis::use_data(england_ltla_shp, overwrite = TRUE)
